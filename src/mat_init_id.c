#include "s_gfx_matrix.h"

void	mat3_init_id(t_mat3 *m3)
{
	m3->data[0][0] = 1.0;
	m3->data[0][1] = 0.0;
	m3->data[0][2] = 0.0;
	m3->data[1][0] = 0.0;
	m3->data[1][1] = 1.0;
	m3->data[1][2] = 0.0;
	m3->data[2][0] = 0.0;
	m3->data[2][1] = 0.0;
	m3->data[2][2] = 1.0;
}

void	mat4_init_id(t_mat4 *m4)
{
	m4->data[0][0] = 1.0;
	m4->data[0][1] = 0.0;
	m4->data[0][2] = 0.0;
	m4->data[0][3] = 0.0;
	m4->data[1][0] = 0.0;
	m4->data[1][1] = 1.0;
	m4->data[1][2] = 0.0;
	m4->data[1][3] = 0.0;
	m4->data[2][0] = 0.0;
	m4->data[2][1] = 0.0;
	m4->data[2][2] = 1.0;
	m4->data[2][3] = 0.0;
	m4->data[3][0] = 0.0;
	m4->data[3][1] = 0.0;
	m4->data[3][2] = 0.0;
	m4->data[3][3] = 1.0;
}
