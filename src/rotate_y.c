#include "gfxwtc.h"

void	rotate_y(t_mat4 *m4, double rad)
{
	m4->data[0][0] = cos(rad);
	m4->data[0][1] = 0;
	m4->data[0][2] = -sin(rad);
	m4->data[0][3] = 0;

	m4->data[1][0] = 0;
	m4->data[1][1] = 1;
	m4->data[1][2] = 0;
	m4->data[1][3] = 0;

	m4->data[2][0] = sin(rad);
	m4->data[2][1] = 0;
	m4->data[2][2] = cos(rad);
	m4->data[2][3] = 0;

	m4->data[3][0] = 0;
	m4->data[3][1] = 0;
	m4->data[3][2] = 0;
	m4->data[3][3] = 1;
}
